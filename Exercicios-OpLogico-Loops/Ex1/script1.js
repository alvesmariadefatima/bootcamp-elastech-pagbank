let numeroDia = Number(prompt("Digite um número de 1 a 7: "));

if(numeroDia === 1) {
    alert("Domingo")
} else if (numeroDia === 2) {
    alert("Segunda")
} else if (numeroDia === 3) {
    alert("Terça")
} else if (numeroDia === 4) {
    alert("Quarta")
} else if (numeroDia === 5) {
    alert("Quinta")
} else if (numeroDia === 6) {
    alert("Sexta")
} else if (numeroDia === 7) {
    alert("Sábado")
} else {
    alert("Número inválido")
}